#!/usr/bin/env node
/**
 * Generates a static TypeScript file with all example_output file contents.
 * Run: node scripts/generate-example-manifest.mjs
 * Output: app/example-files.ts
 */
import { readdir, readFile, writeFile, stat } from "fs/promises";
import { join, relative } from "path";

const EXAMPLE_DIR = new URL("../example_output", import.meta.url).pathname;
const OUTPUT_FILE = new URL("../app/example-files.ts", import.meta.url).pathname;

async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const full = join(dir, entry.name);
    if (entry.name === ".DS_Store") continue;
    if (entry.isDirectory()) {
      files.push(...(await walk(full)));
    } else {
      files.push(full);
    }
  }
  return files;
}

async function main() {
  const allFiles = await walk(EXAMPLE_DIR);
  const manifest = {};

  for (const file of allFiles.sort()) {
    const relPath = relative(EXAMPLE_DIR, file);
    const content = await readFile(file, "utf-8");
    manifest[relPath] = content;
  }

  const ts = `// Auto-generated by scripts/generate-example-manifest.mjs — do not edit
export const EXAMPLE_FILES: Record<string, string> = ${JSON.stringify(manifest, null, 2)};
`;

  await writeFile(OUTPUT_FILE, ts, "utf-8");
  console.log(`Generated ${OUTPUT_FILE} with ${Object.keys(manifest).length} files`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
