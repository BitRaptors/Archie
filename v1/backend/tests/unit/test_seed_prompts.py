class TestSupabasePath:

    def test_supabase_path_uses_table_not_postgrest(self):
        """The Supabase code path should use db.table(), never db.postgrest."""
        import inspect
        from seed_prompts import main

        source = inspect.getsource(main)
        # Must not reference .postgrest (the bug we fixed)
        assert ".postgrest" not in source, (
            "seed_prompts.py must not access db.postgrest — "
            "use db.table() via the DatabaseClient interface"
        )

    def test_supabase_path_uses_select_insert_update(self):
        """The Supabase path should use the standard DatabaseClient methods."""
        import inspect
        from seed_prompts import main

        source = inspect.getsource(main)
        # Should use the adapter's table() interface
        assert 'db.table("analysis_prompts")' in source

    @pytest.mark.asyncio
    async def test_supabase_insert_new_prompt(self):
        """When a prompt doesn't exist, Supabase path should INSERT."""
        mock_db = MagicMock()
        mock_query = MagicMock()

        # select().eq().maybe_single().execute() returns no data
        mock_query.select.return_value = mock_query
        mock_query.eq.return_value = mock_query
        mock_query.maybe_single.return_value = mock_query
        mock_query.execute = AsyncMock(return_value=MagicMock(data=None))
        mock_query.insert.return_value = mock_query

        mock_db.table.return_value = mock_query

        # Simulate the upsert logic from seed_prompts
        prompt_data = SAMPLE_PROMPTS["discovery"]
        key = "discovery"

        existing = await (
            mock_db.table("analysis_prompts")
            .select("id")
            .eq("key", key)
            .maybe_single()
            .execute()
        )

        row = {
            "name": prompt_data["name"],
            "category": prompt_data["category"],
            "prompt_template": prompt_data["prompt_template"],
            "variables": prompt_data.get("variables", []),
            "is_default": prompt_data.get("is_default", True),
            "key": key,
            "type": "prompt",
        }

        if existing and existing.data:
            await mock_db.table("analysis_prompts").update(row).eq("key", key).execute()
        else:
            await mock_db.table("analysis_prompts").insert(row).execute()

        # Should have called insert, not update
        mock_query.insert.assert_called_once_with(row)

    @pytest.mark.asyncio
    async def test_supabase_update_existing_prompt(self):
        """When a prompt exists, Supabase path should UPDATE."""
        mock_db = MagicMock()
        mock_query = MagicMock()

        # select().eq().maybe_single().execute() returns existing data
        mock_query.select.return_value = mock_query
        mock_query.eq.return_value = mock_query
        mock_query.maybe_single.return_value = mock_query
        mock_query.execute = AsyncMock(
            return_value=MagicMock(data={"id": "existing-uuid"})
        )
        mock_query.update.return_value = mock_query
        mock_query.insert.return_value = mock_query

        mock_db.table.return_value = mock_query

        prompt_data = SAMPLE_PROMPTS["discovery"]
        key = "discovery"

        existing = await (
            mock_db.table("analysis_prompts")
            .select("id")
            .eq("key", key)
            .maybe_single()
            .execute()
        )

        row = {
            "name": prompt_data["name"],
            "category": prompt_data["category"],
            "prompt_template": prompt_data["prompt_template"],
            "variables": prompt_data.get("variables", []),
            "is_default": prompt_data.get("is_default", True),
            "key": key,
            "type": "prompt",
        }

        if existing and existing.data:
            await mock_db.table("analysis_prompts").update(row).eq("key", key).execute()
        else:
            await mock_db.table("analysis_prompts").insert(row).execute()

        # Should have called update, not insert (insert still called due to mock chaining,
        # but update must have been called)
        mock_query.update.assert_called_once_with(row)


