class TestEnvParsing:

    def test_parses_simple_env(self, tmp_path):
        env_file = tmp_path / ".env.local"
        env_file.write_text("DB_BACKEND=postgres\nDATABASE_URL=postgresql://localhost/test\n")
        result = _read_env(env_file)
        assert result["DB_BACKEND"] == "postgres"
        assert result["DATABASE_URL"] == "postgresql://localhost/test"

    def test_skips_comments(self, tmp_path):
        env_file = tmp_path / ".env.local"
        env_file.write_text("# This is a comment\nDB_BACKEND=supabase\n")
        result = _read_env(env_file)
        assert "# This is a comment" not in result
        assert result["DB_BACKEND"] == "supabase"

    def test_skips_empty_lines(self, tmp_path):
        env_file = tmp_path / ".env.local"
        env_file.write_text("\n\nDB_BACKEND=postgres\n\n")
        result = _read_env(env_file)
        assert result["DB_BACKEND"] == "postgres"

    def test_strips_quotes(self, tmp_path):
        env_file = tmp_path / ".env.local"
        env_file.write_text('ANTHROPIC_API_KEY="sk-ant-test"\n')
        result = _read_env(env_file)
        assert result["ANTHROPIC_API_KEY"] == "sk-ant-test"

    def test_strips_single_quotes(self, tmp_path):
        env_file = tmp_path / ".env.local"
        env_file.write_text("ANTHROPIC_API_KEY='sk-ant-test'\n")
        result = _read_env(env_file)
        assert result["ANTHROPIC_API_KEY"] == "sk-ant-test"

    def test_returns_empty_for_missing_file(self, tmp_path):
        result = _read_env(tmp_path / "nonexistent")
        assert result == {}

    def test_handles_spaces_around_equals(self, tmp_path):
        env_file = tmp_path / ".env.local"
        env_file.write_text("DB_BACKEND = postgres\n")
        result = _read_env(env_file)
        assert result["DB_BACKEND"] == "postgres"


