class TestEndToEndSqlValidity:

    def test_no_unescaped_single_quotes_break_sql(self):
        """Ensure no prompt template contains patterns that would break SQL."""
        prompts, _ = _load_prompts()
        sql = _generate_sql(prompts)

        # Every VALUES(...) block should have balanced quotes
        # A simple check: split on INSERT and verify each block is parseable
        blocks = sql.split("INSERT INTO analysis_prompts")
        for i, block in enumerate(blocks[1:], 1):  # Skip header
            # Count single quotes — should be even (all properly escaped)
            quote_count = block.count("'")
            assert quote_count % 2 == 0, (
                f"Block {i} has odd number of single quotes ({quote_count}) — "
                f"likely an unescaped quote in prompt template"
            )

    def test_all_prompt_keys_present_in_sql(self):
        prompts, _ = _load_prompts()
        sql = _generate_sql(prompts)
        for key in prompts:
            assert f"'{key}'" in sql, f"Prompt key '{key}' not found in generated SQL"
