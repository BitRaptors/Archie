def test_append_block_adds_separator_blank_line():
    existing = "user content\n"
    new_block = "ARCHIE\n"
    out = _append_block(existing, new_block)
    assert out == "user content\n\nARCHIE\n"


def test_append_block_handles_missing_trailing_newline():
    existing = "user content"  # no trailing newline
    new_block = "ARCHIE\n"
    out = _append_block(existing, new_block)
    assert out == "user content\n\nARCHIE\n"


