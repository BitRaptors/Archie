def generate_all(bp: dict, enforcement_rules: list[dict] | None = None) -> dict:
    """Generate all output files from a blueprint dict.

    `enforcement_rules` is the merged contents of `.archie/rules.json` +
    `.archie/platform_rules.json` — when present, an additional
    `.claude/rules/enforcement/` directory (index.md + by-topic/ +
    universal.md) gets emitted with every rule grouped by severity_class.
    Pass `None` (or omit) when rendering a blueprint without access to those files.

    Returns {path: content} for every output file.
    """
    rule_files = []

    builders = [
        _build_architecture_rule,
        _build_patterns_rule,
        _build_technology_rule,
        _build_frontend_rule,
        _build_guidelines_rule,
        _build_pitfalls_rule,
        _build_dev_rules_rule,
        _build_infrastructure_rule,
        _build_data_models_rule,
    ]

    for builder in builders:
        result = builder(bp)
        if result is not None:
            rule_files.append(result)

    # AGENTS.md is the canonical, blueprint-derived doc (vendor-neutral
    # standard read by Cursor, Codex, Aider, Continue, Cline, Cody, and
    # Claude Code). CLAUDE.md is a static pointer (full-overwrite, not
    # mergeable) so any Claude Code session is told where the truth lives
    # without paying duplicate tokens.
    files = {
        "AGENTS.md": generate_agents_md(bp),
        "CLAUDE.md": _CLAUDE_POINTER,
    }

    for rf in rule_files:
        topic = rf["topic"]
        files[f".claude/rules/{topic}.md"] = _render_claude(rf)

    # Enforcement rules topic — only emitted when caller passes the rules
    # list (typically loaded from .archie/rules.json + platform_rules.json).
    if enforcement_rules:
        directory = build_enforcement_directory(enforcement_rules)
        for rel_path, content in directory.items():
            files[f".claude/rules/{rel_path}"] = content

    return files


