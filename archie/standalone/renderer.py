def _generate_agent_body(bp: dict, *, h1: str) -> str:
    """Generate the lean blueprint-derived agent-context body.

    `h1` is the H1 heading (e.g. "AGENTS.md" or "CLAUDE.md"). The rest of
    the body is identical regardless of filename — same blueprint, same
    sections.
    """
    timestamp = datetime.now(timezone.utc).isoformat()
    repo = _get(bp, "meta", "repository", default="Unknown Repository") or "Unknown Repository"
    style = (_get(bp, "meta", "architecture_style")
             or _get(bp, "decisions", "architectural_style", "title")
             or "Not determined")

    lines = []

    # Header
    lines.append(f"# {h1}")
    lines.append("")
    lines.append(f"> Architecture guidance for **{repo}**")
    lines.append(f"> Style: {style}")
    lines.append(f"> Generated: {timestamp}")
    lines.append("")

    # Executive Summary
    summary = _get(bp, "meta", "executive_summary")
    if summary:
        lines.append("## Overview")
        lines.append("")
        lines.append(summary)
        lines.append("")

    # Architecture style
    ad = _get(bp, "decisions", "architectural_style", default={}) or {}
    if ad.get("chosen"):
        lines.append("## Architecture")
        lines.append("")
        lines.append(f"**Style:** {ad['chosen']}")
        structure = _get(bp, "components", "structure_type")
        if structure:
            lines.append(f"**Structure:** {structure}")
        if ad.get("rationale"):
            lines.append("")
            lines.append(ad["rationale"])
        lines.append("")

    # Decision chain (compact — root + top-level forces). Skips
    # malformed force entries (non-dicts) defensively.
    dc = _get(bp, "decisions", "decision_chain", default={}) or {}
    if dc.get("root"):
        lines.append(f"**Root constraint:** {dc['root']}")
        for f in _cap(dc.get("forces") or [], 5):
            if not isinstance(f, dict):
                continue
            lines.append(f"- → {f.get('decision', '')}")
        lines.append("")

    # Trade-offs summary (top 3). Skips malformed entries (non-dicts).
    trade_offs = _get(bp, "decisions", "trade_offs", default=[]) or []
    valid_trade_offs = [to for to in trade_offs if isinstance(to, dict)]
    if valid_trade_offs:
        lines.append("**Key trade-offs:**")
        for to in _cap(valid_trade_offs, 3):
            accept = to.get("accept", "") or to.get("accepted", "")
            benefit = to.get("benefit", "") or to.get("gained", "")
            lines.append(f"- {accept} → {benefit}")
        lines.append("")

    # Deployment
    dep = _as_dict(bp.get("deployment"))
    if dep.get("runtime_environment"):
        lines.append(f"**Runs on:** {dep['runtime_environment']}")
        if dep.get("compute_services"):
            lines.append(f"**Compute:** {', '.join(_as_list(dep['compute_services']))}")
        if dep.get("ci_cd"):
            cleaned = [_clean_str_item(x) for x in dep["ci_cd"]]
            lines.append(f"**CI/CD:** {', '.join(cleaned)}")
        lines.append("")

    # Data Models — concise summary; full lifecycle lives in
    # .claude/rules/data-models.md (rendered by _build_data_models_rule).
    # Section is elided entirely on blueprints with no data surface.
    # Order: AI-written overview, models (most-referenced first), stores
    # (most-data first). Models lead because that's what an editor agent
    # reaches for; stores are reference material.
    data_models = _sort_data_models(bp.get("data_models") or [])
