def _normalize_data_model_fields(m: dict) -> list[dict]:
    """Coerce a data_model's field list to the new {name, type, description}
    shape. Accepts both the new shape and the legacy `key_fields: ["id", ...]`
    string array for blueprints produced before the schema upgrade."""
    fields = m.get("fields")
    if isinstance(fields, list) and fields:
        out: list[dict] = []
        for f in fields:
            if isinstance(f, dict):
                out.append({
                    "name": str(f.get("name", "")),
                    "type": str(f.get("type", "")),
                    "description": str(f.get("description", "")),
                })
            elif isinstance(f, str):
                out.append({"name": f, "type": "", "description": ""})
        return out
    # Legacy fallback: key_fields was a string list.
    legacy = m.get("key_fields") or []
    if isinstance(legacy, list):
        return [
            {"name": str(k), "type": "", "description": ""}
            for k in legacy if isinstance(k, (str, int))
        ]
    return []


def _normalize_data_model_guarantees(m: dict) -> list[str]:
    """Return the guarantees array. Accepts new `guarantees` key, falls back
    to legacy `invariants`."""
    g = m.get("guarantees")
    if isinstance(g, list):
        return [str(x) for x in g if isinstance(x, (str, int))]
    legacy = m.get("invariants")
    if isinstance(legacy, list):
        return [str(x) for x in legacy if isinstance(x, (str, int))]
    return []


def _normalize_data_model_consumers(m: dict) -> list[dict]:
    """Return consumers in the new {object, file, role} shape. Falls back to
    legacy `lifecycle.related_business_logic: ["path/to/file.py"]` by mapping
    each path to a consumer with empty object/role."""
    c = m.get("consumers")
    if isinstance(c, list) and c:
        out: list[dict] = []
        for item in c:
            if isinstance(item, dict):
                out.append({
                    "object": str(item.get("object", "")),
                    "file": str(item.get("file", "")),
                    "role": str(item.get("role", "")),
                })
            elif isinstance(item, str):
                out.append({"object": "", "file": item, "role": ""})
        return out
    lifecycle = m.get("lifecycle") or {}
    if isinstance(lifecycle, dict):
        legacy = lifecycle.get("related_business_logic") or []
        if isinstance(legacy, list):
            return [
                {"object": "", "file": str(p), "role": ""}
                for p in legacy if isinstance(p, str)
            ]
    return []


def _normalize_data_model_lifecycle(m: dict) -> dict:
    """Return the lifecycle dict with how_to_* entries as {prose, example}
    objects. Accepts legacy strings (where prose was the entire value and
    no example existed) and wraps them."""
    lifecycle = m.get("lifecycle") or {}
    if not isinstance(lifecycle, dict):
        return {}
    out: dict = {}
    for key in ("how_to_add", "how_to_modify", "how_to_read"):
        raw = lifecycle.get(key)
        if isinstance(raw, dict):
            out[key] = {
                "prose": str(raw.get("prose", "")),
                "example": str(raw.get("example", "")),
            }
        elif isinstance(raw, str) and raw:
            out[key] = {"prose": raw, "example": ""}
        else:
            out[key] = {"prose": "", "example": ""}
    out["backup_strategy"] = str(lifecycle.get("backup_strategy") or "")
    tests = lifecycle.get("tests") or []
    out["tests"] = [str(t) for t in tests if isinstance(t, str)]
    return out


def _build_data_models_rule(bp: dict):
    """Persistence stores, data models, and per-model lifecycle.

    Renders the full `data_models` + `persistence_stores` blueprint sections
    so an agent touching a model has the full per-model picture (fields with
    business descriptions, data guarantees, consumers with their role, and
    lifecycle prose + code examples) without re-reading the blueprint.
    Returns None when neither section is populated — silently elides for
    repos with no persistence.

    Accepts both the new schema (fields/guarantees/consumers/lifecycle.*.example)
    and the legacy schema (key_fields/invariants/lifecycle.related_business_logic
    /lifecycle.how_to_* as strings) for blueprints produced before the upgrade.
    """
    data_models = _sort_data_models(bp.get("data_models") or [])
    persistence_stores = _sort_persistence_stores(bp.get("persistence_stores") or [])
    if not data_models and not persistence_stores:
        return None

    lines: list[str] = []

    overview = bp.get("data_overview") or ""
    if isinstance(overview, str) and overview.strip():
        lines.append(overview.strip())
        lines.append("")

    if data_models:
        lines.append("## Models")
        lines.append("")
        lines.append(f"_{_DATA_MODELS_SECTION_INTRO}_")
        lines.append("")
        for m in data_models:
            name = m.get("name", "")
            description = m.get("description") or ""
            kind = m.get("kind", "")
            location = m.get("location", "")
            store = m.get("store", "")
            owner = m.get("owned_by_component", "")
            fields = _normalize_data_model_fields(m)
            guarantees = _normalize_data_model_guarantees(m)
            consumers = _normalize_data_model_consumers(m)
            lifecycle = _normalize_data_model_lifecycle(m)

            head = f"### `{name}`"
            if kind:
                head += f" *({kind})*"
            lines.append(head)
            if description.strip():
                lines.append("")
                lines.append(description.strip())
                lines.append("")
            if location:
                lines.append(f"- **Location:** `{location}`")
            if store:
                lines.append(f"- **Store:** `{store}`")
            if owner:
                lines.append(f"- **Owner:** `{owner}`")

            if fields:
                lines.append("")
                lines.append("**Fields:**")
                lines.append("")
                lines.append("| Field | Type | Description |")
                lines.append("|---|---|---|")
                for f in fields:
                    fn = f["name"]
                    ft = f["type"] or "—"
                    fd = f["description"] or ""
                    # Escape pipes in description so the table doesn't break.
                    fd = fd.replace("|", "\\|")
                    lines.append(f"| `{fn}` | {ft} | {fd} |")
                lines.append("")

            if guarantees:
                lines.append("**Data Guarantees:**")
                for g in guarantees:
                    lines.append(f"- {g}")
                lines.append("")

            if consumers:
                lines.append("**Consumers:**")
                for c in consumers:
                    obj = c["object"]
                    fpath = c["file"]
                    role = c["role"]
                    head_str = f"- `{obj}`" if obj else "-"
                    if fpath:
                        head_str += f" — `{fpath}`" if obj else f" `{fpath}`"
                    if role:
                        head_str += f": {role}"
                    lines.append(head_str)
                lines.append("")

            lifecycle_has_content = any(
                lifecycle.get(k, {}).get("prose") or lifecycle.get(k, {}).get("example")
                for k in ("how_to_add", "how_to_modify", "how_to_read")
            ) or lifecycle.get("backup_strategy") or lifecycle.get("tests")
            if lifecycle_has_content:
                lines.append("**Lifecycle:**")
                for label, key in (
                    ("How to add", "how_to_add"),
                    ("How to modify", "how_to_modify"),
                    ("How to read", "how_to_read"),
                ):
                    entry = lifecycle.get(key) or {}
                    prose = entry.get("prose") or ""
                    example = entry.get("example") or ""
                    if not prose and not example:
                        continue
                    lines.append(f"- *{label}:* {prose}" if prose else f"- *{label}:*")
                    if example:
                        lines.append("")
                        lines.append("  ```")
                        for ex_line in example.split("\n"):
                            lines.append(f"  {ex_line}")
                        lines.append("  ```")
                        lines.append("")
                backup = lifecycle.get("backup_strategy") or ""
                if backup:
                    lines.append(f"- *Backup:* {backup}")
                tests = lifecycle.get("tests") or []
                if tests:
                    lines.append(
                        "- *Tests:* " + ", ".join(f"`{p}`" for p in tests)
                    )
            lines.append("")

    if persistence_stores:
        lines.append("## Persistence Stores")
        lines.append("")
        lines.append(f"_{_PERSISTENCE_STORES_SECTION_INTRO}_")
        lines.append("")
        for s in persistence_stores:
            name = s.get("name", "")
            description = s.get("description") or ""
            engine = s.get("engine", "")
            role = s.get("role", "")
            mig = s.get("migrations_dir", "")
            backup = s.get("backup_strategy", "")
            owned = s.get("owned_models") or []
            writers = s.get("writers") or []
            lines.append(f"### `{name}`")
            if description.strip():
                lines.append("")
                lines.append(description.strip())
                lines.append("")
            if engine:
                lines.append(f"- **Engine:** {engine}")
            if role:
                lines.append(f"- **Role:** {role}")
            if mig:
                lines.append(f"- **Migrations dir:** `{mig}`")
            if backup:
                lines.append(f"- **Backup:** {backup}")
            if owned:
                lines.append(f"- **Lives here:** {', '.join(owned)}")
            if writers:
                # Derived from data_models[*].owned_by_component during finalize
                # (see finalize.py::_derive_persistence_writers). Empty on
                # blueprints rendered before that derivation existed.
                lines.append(f"- **Written by:** {', '.join(writers)}")
            lines.append("")

    return {
        "topic": "data-models",
        "body": "\n".join(lines).rstrip(),
        "description": "Models (sorted by most-referenced) and the stores they live in, with descriptions and per-model lifecycle",
        "always_apply": False,
        "globs": [],
    }


# Keywords that mark a command as "essential" — what an agent reaches for
# in 80% of sessions. Anything not matching one of these gets pushed to
# the technology.md catalog and out of the lean root file.
_ESSENTIAL_COMMAND_KEYWORDS = (
    "build", "test", "lint", "fmt", "format", "run", "serve", "start", "dev", "up",
)
_ESSENTIAL_COMMAND_LIMIT = 8


def _essential_commands(run_commands: dict) -> dict:
    """Curate a lean subset of run_commands for the root CLAUDE.md / AGENTS.md.

    Scores each command by keyword match (build / test / lint / fmt / run /
    serve / start / dev / up) and prefers exact matches and shorter names —
    so `build` outranks `build-server` and `test` outranks `test-nocache`.
    Capped at 8 entries regardless of project size; the full catalog lives
    in `.claude/rules/technology.md`.
    """
    scored: list[tuple[tuple[bool, int], str, str]] = []
    for k, v in run_commands.items():
        kl = k.lower()
        for kw in _ESSENTIAL_COMMAND_KEYWORDS:
            if kw in kl:
                # Sort key: exact-name match wins, then shorter name wins.
                # Negative length so larger tuple = higher priority on desc sort.
                scored.append(((kl == kw, -len(k)), k, v))
                break
    scored.sort(reverse=True)
    return {k: v for _, k, v in scored[:_ESSENTIAL_COMMAND_LIMIT]}


def _build_technology_rule(bp: dict):
    """Tech stack, project structure, code templates, testing tooling.

    Surfaces what was previously only in AGENTS.md so a single topic file
    captures the project's tech footprint. Agents land here when the question
    is "what stack are we on / where does what live / how do tests run."
    """
    stack = _get(bp, "technology", "stack", default=[]) or []
    project_structure = _get(bp, "technology", "project_structure")
    templates = _get(bp, "technology", "templates", default=[]) or []
    run_commands = _get(bp, "technology", "run_commands", default={}) or {}
    test_commands = {
        k: v for k, v in run_commands.items()
        if any(t in k.lower() for t in ("test", "lint", "check", "verify"))
    }
    test_stack = [
        e for e in stack
        if (e.get("category") or "").lower() in ("testing", "linting")
    ]

    if not (stack or project_structure or templates or test_commands or test_stack):
        return None

    lines: list[str] = []

    # Tech Stack — grouped by category for fast scanning
    if stack:
        lines.append("## Tech Stack")
        lines.append("")
        by_cat = defaultdict(list)
        for entry in stack:
            version = f" {entry['version']}" if entry.get("version") else ""
            by_cat[entry.get("category", "Other")].append(f"{entry.get('name', '')}{version}")
        for cat in sorted(by_cat.keys()):
            techs = ", ".join(by_cat[cat])
            lines.append(f"- **{cat}:** {techs}")
        lines.append("")

    # Project Structure — ASCII tree (Wave-1 generated)
    if project_structure:
        lines.append("## Project Structure")
        lines.append("")
        lines.append("```")
        lines.append(project_structure)
        lines.append("```")
        lines.append("")

    # Run Commands — the FULL catalog. The lean root only surfaces the
    # essential subset (build / test / lint / fmt / run/serve/up); the
    # rest live here so agents looking for a specific make target know
    # where to find it.
    if run_commands:
        lines.append("## Run Commands")
        lines.append("")
        lines.append("```bash")
        for cmd_name, cmd_value in run_commands.items():
            lines.append(f"# {cmd_name}")
            lines.append(cmd_value)
        lines.append("```")
        lines.append("")

    # Code Templates — concrete shapes for adding new components
    if templates:
        lines.append("## Code Templates")
        lines.append("")
        for tmpl in _cap(templates, 6):
            code = tmpl.get("code")
            if not code:
                continue
            lines.append(f"### {tmpl.get('component_type', '')}: {tmpl.get('description', '')}")
            lines.append("")
            if tmpl.get("file_path_template"):
                lines.append(f"File: `{tmpl['file_path_template']}`")
                lines.append("")
            lines.append("```")
            lines.append(code)
            lines.append("```")
            lines.append("")

    # Testing
    if test_stack or test_commands:
        lines.append("## Testing")
        lines.append("")
        if test_stack:
            for entry in test_stack:
                version = f" {entry['version']}" if entry.get("version") else ""
                lines.append(f"- **{entry.get('name', '')}{version}** — {entry.get('purpose', '')}")
            lines.append("")
        if test_commands:
            lines.append("```bash")
            for cmd_name, cmd_value in test_commands.items():
                lines.append(f"# {cmd_name}")
                lines.append(cmd_value)
            lines.append("```")
            lines.append("")

    return {
        "topic": "technology",
        "body": "\n".join(lines).rstrip(),
        "description": "Tech stack, project structure, code templates, testing tooling",
        "always_apply": True,
        "globs": [],
    }


