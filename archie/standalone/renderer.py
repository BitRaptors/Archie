#!/usr/bin/env python3
"""Archie standalone renderer — generates CLAUDE.md, AGENTS.md, and rule files from blueprint.

Run: python3 renderer.py /path/to/project
Reads: .archie/blueprint.json
Writes: CLAUDE.md, AGENTS.md, .claude/rules/*.md

CLAUDE.md and AGENTS.md support hand-authored content. The renderer bracketed
its generated block with `archie:generated:start` / `archie:generated:end`
HTML comment markers. On regenerate, only the bracketed block is rewritten —
content outside the markers is preserved. First-time regeneration against a
hand-authored file appends the generated block at the end so the user's
original content survives.

Zero dependencies beyond Python 3.9+ stdlib.
"""
from __future__ import annotations

import ast
import json
import re
import sys
from collections import defaultdict
from datetime import datetime, timezone
from pathlib import Path


# ---------------------------------------------------------------------------
# Merge markers for user-editable files (CLAUDE.md / AGENTS.md)
# ---------------------------------------------------------------------------

ARCHIE_MARKER_START = "<!-- archie:generated:start -->"
ARCHIE_MARKER_END = "<!-- archie:generated:end -->"


# ---------------------------------------------------------------------------
# Comprehensive-depth render switch
# ---------------------------------------------------------------------------
#
# Default depth trims several blueprint sections to a fixed cap so the rendered
# CLAUDE.md / AGENTS.md stay lean. Comprehensive depth (the `--comprehensive`
# CLI flag, set in main()) lifts those caps so every blueprint item renders.
# Implemented as a module-level switch read by `_cap` rather than a parameter
# threaded through every render function — the slice sites are scattered across
# several builders, and a flag keeps the change small and localized.

_COMPREHENSIVE = False


def _cap(seq, n):
    """Return `seq` trimmed to `n` in default depth; the full `seq` when the
    comprehensive switch is on."""
    return seq if _COMPREHENSIVE else seq[:n]

# Files that may coexist with hand-authored content. Everything else the
# renderer emits (rule files under .claude/rules/, CLAUDE.md pointer) is
# fully Archie-owned — those still get a straight overwrite because the
# user doesn't edit them.
#
# AGENTS.md is canonical (vendor-neutral standard read by Cursor, Codex,
# Aider, Continue, Cline, Cody, and Claude Code itself). CLAUDE.md is a
# thin pointer to AGENTS.md — full-overwrite, never mergeable, so it can
# never drift from the source of truth. Per-folder context still uses
# CLAUDE.md (intent_layer.py) since nested context is dominantly a Claude
# Code idiom and other tools rarely act on it.
MERGEABLE_FILES = frozenset({"AGENTS.md"})


def _wrap_with_markers(content: str) -> str:
    """Wrap generated content in Archie markers with a provenance stamp."""
    stamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%MZ")
    provenance = f"<!-- Regenerated by Archie on {stamp}. Edits between the archie:generated markers will be overwritten; edit outside them to keep changes. -->"
    return f"{ARCHIE_MARKER_START}\n{provenance}\n\n{content.rstrip()}\n{ARCHIE_MARKER_END}\n"


def _replace_marked_block(existing: str, new_block: str) -> str:
    """Replace the content between Archie markers in `existing` with `new_block`.

    Preserves every byte of `existing` outside the markers. If the end marker
    appears before the start marker (malformed), falls back to append mode
    rather than corrupting the file.
    """
    start_idx = existing.find(ARCHIE_MARKER_START)
    end_idx = existing.find(ARCHIE_MARKER_END, start_idx + len(ARCHIE_MARKER_START))
    if start_idx < 0 or end_idx < 0:
        return _append_block(existing, new_block)
    end_idx_inclusive = end_idx + len(ARCHIE_MARKER_END)
    return existing[:start_idx] + new_block.rstrip() + existing[end_idx_inclusive:]


def _append_block(existing: str, new_block: str) -> str:
    """Append `new_block` after `existing` with a clean blank line separator."""
    sep = "" if existing.endswith("\n") else "\n"
    return existing + sep + "\n" + new_block


def render_mergeable(path: Path, generated_content: str) -> str:
    """Compute the final content to write for CLAUDE.md / AGENTS.md.

    Three cases:
    - Path does not exist → return a fresh wrapped block.
    - Path exists AND contains Archie markers → replace only the bracketed block.
    - Path exists AND has no markers → append the wrapped block at the end,
      preserving the user's hand-authored content untouched.
    """
    wrapped = _wrap_with_markers(generated_content)
    if not path.exists():
        return wrapped
    existing = path.read_text()
    if ARCHIE_MARKER_START in existing and ARCHIE_MARKER_END in existing:
        return _replace_marked_block(existing, wrapped)
    return _append_block(existing, wrapped)


