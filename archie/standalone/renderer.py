def _clean_str_item(item) -> str:
    """Convert any item (str, dict, etc.) to readable text."""
    if isinstance(item, dict):
        # Agent returned a dict — extract meaningful fields
        parts = []
        for k, v in item.items():
            if isinstance(v, list):
                parts.append(f"{k}: {', '.join(str(x) for x in v)}")
            elif v:
                parts.append(f"{k}: {v}")
        return "; ".join(parts) if parts else str(item)
    s = str(item)
    stripped = s.strip()
    if not (stripped.startswith("{") and stripped.endswith("}")):
        return s
    try:
        obj = ast.literal_eval(stripped)
    except (ValueError, SyntaxError):
        return s
    if not isinstance(obj, dict):
        return s
    parts = []
    for k, v in obj.items():
        if isinstance(v, list):
            parts.append(f"{k}: {', '.join(str(x) for x in v)}")
        elif v:
            parts.append(f"{k}: {v}")
    return "; ".join(parts)


def _get(obj, *keys, default=None):
    """Safely traverse nested dicts."""
    current = obj
    for k in keys:
        if isinstance(current, dict):
            current = current.get(k, default)
        else:
            return default
        if current is None:
            return default
    return current


def _as_dict(val, default=None):
    """Ensure a value is a dict. If it's a string, wrap it."""
    if isinstance(val, dict):
        return val
    if default is not None:
        return default
    return {}


def _as_list(val):
    """Ensure a value is a list."""
    if isinstance(val, list):
        return val
    if isinstance(val, str) and val:
        return [val]
    return []


def _str_list(val) -> list[str]:
    """Coerce a value to a list of non-empty strings.

    Used for fields like ``violation_signals`` and ``violation_keywords``
    where a malformed blueprint might pass a string, None, or a list
    containing non-strings. Prevents per-character bullet spam and
    silently drops non-string entries.
    """
    if isinstance(val, list):
        return [s for s in val if isinstance(s, str) and s.strip()]
    if isinstance(val, str) and val.strip():
        return [val]
    return []


def _escape_table_cell(val) -> str:
    """Escape a value for safe inclusion in a markdown table cell.

    Replaces ``|`` with ``\\|`` and newlines with spaces so a malformed
    field doesn't shred the table layout. Coerces non-strings via ``str()``.
    """
    if val is None:
        return ""
    if not isinstance(val, str):
        val = str(val)
    return val.replace("|", r"\|").replace("\n", " ").strip()


