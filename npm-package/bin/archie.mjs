#!/usr/bin/env node

import { mkdirSync, writeFileSync, readFileSync, existsSync, chmodSync, unlinkSync, readdirSync, rmSync } from "fs";
import { join, resolve } from "path";
import { fileURLToPath } from "url";
import { dirname } from "path";
import { execSync } from "child_process";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ASSETS = join(__dirname, "..", "assets");

const CYAN = "\x1b[36m";
const GREEN = "\x1b[32m";
const DIM = "\x1b[2m";
const RESET = "\x1b[0m";
const BOLD = "\x1b[1m";

// Parse CLI args: archie.mjs <project_root> [--target=auto|claude|codex|both]
function parseArgs(argv) {
  const positional = [];
  let target = "auto";
  for (const arg of argv.slice(2)) {
    if (arg.startsWith("--target=")) {
      target = arg.slice("--target=".length);
    } else if (arg === "--target") {
      console.error("ERROR: --target requires a value, e.g. --target=codex");
      process.exit(2);
    } else if (arg.startsWith("--")) {
      console.error(`ERROR: unknown flag ${arg}`);
      process.exit(2);
    } else {
      positional.push(arg);
    }
  }
  const validTargets = new Set(["auto", "claude", "codex", "both"]);
  if (!validTargets.has(target)) {
    console.error(`ERROR: invalid --target=${target}. Must be one of: auto, claude, codex, both.`);
    process.exit(2);
  }
  return { projectRoot: resolve(positional[0] || "."), target };
}

function resolveTarget(target) {
  if (target !== "auto") return target;
  const home = process.env.HOME || process.env.USERPROFILE || "";
  const hasClaude = home && existsSync(join(home, ".claude"));
  const hasCodex = home && existsSync(join(home, ".codex"));
  if (hasClaude && hasCodex) return "both";
  if (hasCodex) return "codex";
  if (hasClaude) return "claude";
  console.log(`  ${DIM}⚠ no Claude or Codex CLI detected; defaulting to --target=claude${RESET}`);
  return "claude";
}

function installSharedAssets(projectRoot) {
  const archieDir = join(projectRoot, ".archie");
  mkdirSync(archieDir, { recursive: true });

  // Remove all .py files from .archie/ (scripts only, not data)
  if (existsSync(archieDir)) {
    for (const f of readdirSync(archieDir)) {
      if (f.endsWith(".py")) {
        try { unlinkSync(join(archieDir, f)); } catch {}
      }
    }
  }

  // Remove platform_rules.json (will be re-copied)
  const platformRulesPath = join(archieDir, "platform_rules.json");
  if (existsSync(platformRulesPath)) {
    try { unlinkSync(platformRulesPath); } catch {}
  }

  // Copy standalone Python scripts (target-agnostic)
  for (const script of ["_common.py", "scanner.py", "refresh.py", "intent_layer.py", "renderer.py", "install_hooks.py", "merge.py", "finalize.py", "validate.py", "viewer.py", "drift.py", "extract_output.py", "arch_review.py", "measure_health.py", "check_rules.py", "detect_cycles.py"]) {
    const src = join(ASSETS, script);
    const dest = join(archieDir, script);
    if (existsSync(src)) {
      writeFileSync(dest, readFileSync(src, "utf8"));
      chmodSync(dest, 0o755);
      console.log(`  ${GREEN}✓${RESET} .archie/${script}`);
    }
  }

  // Copy data files
  for (const dataFile of ["platform_rules.json"]) {
    const src = join(ASSETS, dataFile);
    const dest = join(archieDir, dataFile);
    if (existsSync(src)) {
      writeFileSync(dest, readFileSync(src, "utf8"));
      console.log(`  ${GREEN}✓${RESET} .archie/${dataFile}`);
    }
  }

  // Copy shared sub-agent prompts (target-agnostic) — NEW in Task 10
  const promptsDir = join(archieDir, "prompts");
  mkdirSync(promptsDir, { recursive: true });
  const PROMPTS_ASSETS = join(ASSETS, "prompts");
  if (existsSync(PROMPTS_ASSETS)) {
    for (const f of readdirSync(PROMPTS_ASSETS)) {
      if (f.endsWith(".md")) {
        const src = join(PROMPTS_ASSETS, f);
        const dest = join(promptsDir, f);
        writeFileSync(dest, readFileSync(src, "utf8"));
        console.log(`  ${GREEN}✓${RESET} .archie/prompts/${f}`);
      }
    }
  }
}

function installClaude(projectRoot) {
  const claudeCommands = join(projectRoot, ".claude", "commands");
  mkdirSync(claudeCommands, { recursive: true });

  // Clean previous Claude command assets
  if (existsSync(claudeCommands)) {
    for (const f of readdirSync(claudeCommands)) {
      if (f.startsWith("archie-") && f.endsWith(".md")) {
        try { unlinkSync(join(claudeCommands, f)); } catch {}
      }
    }
  }

  // Remove .claude/hooks/ entirely (will be regenerated by install_hooks.py)
  const hooksDir = join(projectRoot, ".claude", "hooks");
  if (existsSync(hooksDir)) {
    try { rmSync(hooksDir, { recursive: true, force: true }); } catch {}
  }

  // Remove hooks section from settings.local.json (will be regenerated by install_hooks.py)
  const settingsPath = join(projectRoot, ".claude", "settings.local.json");
  if (existsSync(settingsPath)) {
    try {
      const settings = JSON.parse(readFileSync(settingsPath, "utf8"));
      if (settings.hooks) {
        delete settings.hooks;
        writeFileSync(settingsPath, JSON.stringify(settings, null, 2) + "\n");
      }
    } catch {}
  }

  // Copy Claude Code commands
  for (const cmd of ["archie-scan.md", "archie-deep-scan.md", "archie-viewer.md"]) {
    const src = join(ASSETS, cmd);
    const dest = join(claudeCommands, cmd);
    if (existsSync(src)) {
      writeFileSync(dest, readFileSync(src, "utf8"));
      console.log(`  ${GREEN}✓${RESET} .claude/commands/${cmd}`);
    }
  }
}

const { projectRoot, target } = parseArgs(process.argv);
const resolvedTarget = resolveTarget(target);

console.log("");
console.log(`${BOLD}${CYAN}  Archie${RESET} — architecture enforcement for AI coding agents`);
console.log(`  ${DIM}target: ${resolvedTarget}${RESET}`);
console.log("");

installSharedAssets(projectRoot);
if (resolvedTarget === "claude" || resolvedTarget === "both") {
  installClaude(projectRoot);
}
// installCodex is added in Task 11

const archieDir = join(projectRoot, ".archie");

// 4. Check Python and run install_hooks.py (sets up hooks + permissions)
let hasPython = false;
try {
  execSync("python3 --version", { stdio: "ignore" });
  hasPython = true;
} catch { /* noop */ }

if (hasPython) {
  try {
    execSync(`python3 "${join(archieDir, "install_hooks.py")}" "${projectRoot}"`, { stdio: "pipe" });
    console.log(`  ${GREEN}✓${RESET} hooks + permissions installed`);
  } catch (e) {
    console.log(`  ${DIM}⚠ hook installation failed (non-critical)${RESET}`);
  }
} else {
  console.log("");
  console.log(`  ⚠ python3 not found — hooks not installed, scanner needs Python 3.9+`);
}

// Done
console.log("");
console.log(`${BOLD}  Installed!${RESET}`);
console.log("");
console.log(`  Next steps:`);
console.log(`  1. Open this project in ${BOLD}Claude Code${RESET}`);
console.log(`  2. Run ${BOLD}/archie-scan${RESET} for a fast architecture health check (1-3 min)`);
console.log(`  3. Run ${BOLD}/archie-deep-scan${RESET} for a comprehensive baseline (15-20 min)`);
console.log("");

console.log(`  ${DIM}What gets generated:${RESET}`);
console.log(`  ${DIM}  CLAUDE.md            — architecture context for AI agents${RESET}`);
console.log(`  ${DIM}  AGENTS.md            — multi-agent guidance${RESET}`);
console.log(`  ${DIM}  .claude/hooks/       — real-time architecture enforcement${RESET}`);
console.log(`  ${DIM}  per-folder CLAUDE.md — directory-level context${RESET}`);
console.log("");
